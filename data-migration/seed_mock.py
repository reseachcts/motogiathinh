# ── Orchestrator ─────────────────────────────────────────────────────────────

async def main():
    print("=" * 60)
    print("SEED MOCK DATA (procedural, cutoff =", TIME_CUTOFF, ")")
    print("=" * 60)
    if not DATA_DIR.exists():
        raise SystemExit(f"Mock CSV dir not found: {DATA_DIR}")

    conn = await asyncpg.connect(DB_URL)
    try:
        print("Wiping domain tables ...")
        await conn.execute(WIPE_SQL)

        id_map = {"branches": {}, "users": {}, "teachers": {}}
        await seed_branches(conn, id_map)
        await seed_users(conn, id_map)
        await seed_teachers(conn, id_map)
        vehicles = await seed_vehicles(conn, id_map)
        fee_plans_by_licence = await seed_fee_plans(conn)
        promos_by_licence = await seed_promotions(conn)
        course_type_id = await seed_course_type(conn)

        # Branch + staff maps for generation. Both 'staff' and 'user' roles
        # can be responsibleStaffId on students (user is just a more
        # restricted variant of staff with no Tổ chức edit + filtered audit).
        branch_uuids = list(id_map["branches"].values())
        staff_by_branch = {bid: [] for bid in branch_uuids}
        rows = read_csv("accounts")
        for r in rows:
            if r["role"] not in ("staff", "user"):
                continue
            br = id_map["branches"].get(r.get("branchId"))
            uid = id_map["users"].get(r["id"])
            if br and uid:
                staff_by_branch[br].append(uid)
        # Ensure every branch has at least one staffer for FK resolution
        for bid, lst in staff_by_branch.items():
            if not lst:
                lst.extend(list(id_map["users"].values())[:1])

        print("Generating classes, students, payments, rentals ...")
        classes = gen_classes(branch_uuids, course_type_id)
        students = gen_students(
            classes, fee_plans_by_licence, promos_by_licence,
            staff_by_branch, students_per_class=10,
        )
        payments, bl_counter = gen_payments(students)
        rentals = gen_rentals(students, vehicles, bl_counter)

        await insert_classes(conn, classes)
        await insert_students(conn, students)
        await insert_payments(conn, payments + rentals)

        # Derive notifications from the payment + class state. Receivers are
        # the responsible staff (or admin fallback). 3 flavors:
        # exam-soon-unpaid, payment_due-30d-zero, document_incomplete.
        from collections import defaultdict
        from decimal import Decimal
        paid_by_student = defaultdict(lambda: Decimal(0))
        for p in payments:  # rentals don't reduce tuition balance
            paid_by_student[p["student_id"]] += p["so_tien"]
        classes_by_id = {c["id"]: c for c in classes}
        admin_uid = next(
            (uid for sid, uid in id_map["users"].items() if sid == "u-admin"),
            list(id_map["users"].values())[0],
        )
        notifications = gen_notifications(
            students, classes_by_id, dict(paid_by_student),
            staff_by_branch, admin_uid,
        )
        await insert_notifications(conn, notifications)

        activity = gen_activity_log(students, classes, payments + rentals, admin_uid)
        await insert_activity_log(conn, activity)

        print("\nSummary:")
        for table in ("branches", "users", "instructors", "vehicles", "fee_plans",
                      "promotions", "classes", "students", "class_enrollments",
                      "payments", "notifications", "audit_logs"):
            n = await conn.fetchval(f"SELECT COUNT(*) FROM {table}")
            print(f"  {table:<22} {n:>6}")
        print("\nLogin: admin@motogiathinh.centersai / admin")
    finally:
        await conn.close()


if __name__ == "__main__":
    asyncio.run(main())
