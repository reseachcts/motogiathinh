"""seed_mock.py — wipe domain tables and seed a procedurally-generated dataset.

DESTRUCTIVE: TRUNCATEs every domain table CASCADE before inserting.
Always run `make backup` first (Makefile wires this as a dependency).

Small static entities come from `data-migration/mock_data/*.csv`:
  branches, accounts, fee_plans, promotions, teachers, vehicles
Big tables are generated by `seed_mock_gen.py`:
  classes (102), students (~1020), payments (~1500), rentals (~120)

Run inside the backend container:
    docker compose exec backend python /app/data-migration/seed_mock.py
"""

import asyncio
import csv
import os
import random
import uuid
from datetime import date, datetime
from decimal import Decimal
from pathlib import Path

import asyncpg
import bcrypt
import sys

sys.path.insert(0, str(Path(__file__).parent))
from seed_mock_gen import (
    TIME_CUTOFF, gen_classes, gen_students, gen_payments, gen_rentals,
    gen_notifications, gen_activity_log,
)

DB_URL = os.getenv(
    "DATABASE_URL",
    "postgresql://mgt:mgt_secret@localhost:5432/motogiathinh",
).replace("postgresql+asyncpg://", "postgresql://")

DATA_DIR = Path(__file__).parent / "mock_data"

LIC_TO_DB = {"A": "A2", "A1": "A1"}
STATUS_TO_DB = {"đang hoạt động": "active", "đang bảo trì": "maintenance"}
FEE_PLAN_TO_LICENCE = {"fee-a": "A", "fee-a1": "A1"}

random.seed(42)  # reproducible mock data across runs


def read_csv(name):
    with open(DATA_DIR / f"{name}.csv", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def truthy(s): return str(s or "").strip().lower() in ("true", "1", "yes")
def hash_pw(plain): return bcrypt.hashpw(plain.encode(), bcrypt.gensalt()).decode()


WIPE_SQL = "TRUNCATE TABLE payments, payment_plans, class_enrollments, notifications, audit_logs, student_contacts, students, classes, course_types, vehicles, promotions, fee_plans, instructors, branches, users RESTART IDENTITY CASCADE"


# ── Small static seeders (from CSV) ──────────────────────────────────────────

async def seed_branches(conn, id_map):
    rows = read_csv("branches")
    for r in rows:
        uid = uuid.uuid4()
        id_map["branches"][r["id"]] = uid
        await conn.execute(
            """INSERT INTO branches (id, ma_chi_nhanh, ten_chi_nhanh, dia_chi, is_active,
                slug, created_at, updated_at)
               VALUES ($1, $2, $3, $4, TRUE, $5, NOW(), NOW())""",
            uid, r["id"].upper().replace("-", ""), r["name"],
            r.get("address") or None, r["id"],
        )
    print(f"  branches: {len(rows)}")


PERMISSION_RESOURCES = [
    "students", "payments", "classes", "branches", "accounts",
    "vehicles", "teachers", "fee_plans", "promotions", "activity_log",
]


async def seed_users(conn, id_map):
    rows = read_csv("accounts")
    pw = hash_pw("admin")
    for r in rows:
        uid = uuid.uuid4()
        id_map["users"][r["id"]] = uid
        branch_uuid = id_map["branches"].get(r.get("branchId")) if r.get("branchId") else None
        eff_branch = None if r["role"] == "admin" else branch_uuid
        await conn.execute(
            """INSERT INTO users (id, email, phone, password_hash, full_name, role,
                branch_id, is_active, is_verified, created_at, updated_at)
               VALUES ($1, $2, $3, $4, $5, $6, $7, $8, TRUE, NOW(), NOW())""",
            uid, r["email"], r.get("phone") or None, pw, r["name"],
            r["role"], eff_branch, truthy(r.get("active")),
        )
        # Permissions backfill: admin role bypasses but we still insert
        # all-true rows for UI symmetry. Huy keeps activity_log all-false
        # (preserves the "restricted staff" demo case from prior phases).
        for res in PERMISSION_RESOURCES:
            is_huy_audit = (r["email"] == "huy@motogiathinh.vn" and res == "activity_log")
            cr, rd, up, dl = (False, False, False, False) if is_huy_audit else (True, True, True, True)
            await conn.execute(
                """INSERT INTO user_permissions
                     (id, user_id, resource, can_create, can_read, can_update, can_delete,
                      created_at, updated_at)
                   VALUES (gen_random_uuid(), $1, $2, $3, $4, $5, $6, NOW(), NOW())
                   ON CONFLICT (user_id, resource) DO NOTHING""",
                uid, res, cr, rd, up, dl,
            )
    # branches.manager_id patch
    for r in read_csv("branches"):
        m = id_map["users"].get(r.get("manager_id")) if r.get("manager_id") else None
        if m:
            await conn.execute("UPDATE branches SET manager_id=$1 WHERE id=$2",
                               m, id_map["branches"][r["id"]])
    print(f"  users: {len(rows)}")


async def seed_teachers(conn, id_map):
    rows = read_csv("teachers")
    pw = hash_pw("teacher")
    for r in rows:
        ghost = uuid.uuid4()
        branch_uuid = id_map["branches"].get(r.get("branchId"))
        await conn.execute(
            """INSERT INTO users (id, email, password_hash, full_name, role, branch_id,
                is_active, is_verified, created_at, updated_at)
               VALUES ($1, $2, $3, $4, 'staff', $5, FALSE, TRUE, NOW(), NOW())""",
            ghost, f"{r['id']}@teachers.motogiathinh.local", pw, r["name"], branch_uuid,
        )
        instr = uuid.uuid4()
        id_map["teachers"][r["id"]] = instr
        try: years = int(r.get("yearsExp") or 0)
        except ValueError: years = 0
        hired = date(date.today().year - years, 1, 1)
        await conn.execute(
            """INSERT INTO instructors (id, branch_id, user_id, ma_giao_vien, ho_ten,
                so_dien_thoai, ngay_vao_lam, is_active, rating_avg, total_reviews,
                created_at, updated_at)
               VALUES ($1, $2, $3, $4, $5, $6, $7, TRUE, 0, 0, NOW(), NOW())""",
            instr, branch_uuid, ghost, r["id"].upper(), r["name"],
            r.get("phone") or "", hired,
        )
    print(f"  teachers + ghost users: {len(rows)}")


async def seed_vehicles(conn, id_map):
    rows = read_csv("vehicles")
    vehicles_out = []
    for r in rows:
        uid = uuid.uuid4()
        branch_uuid = id_map["branches"].get(r.get("branchId"))
        if not branch_uuid:
            continue
        licence_wire = r.get("licence") or "A1"
        loai = LIC_TO_DB.get(licence_wire, "A1")
        status = STATUS_TO_DB.get((r.get("status") or "").lower(), "active")
        try: year = int(r.get("year") or 0) or None
        except ValueError: year = None
        rental_price = Decimal(50000)
        await conn.execute(
            """INSERT INTO vehicles (id, branch_id, bien_so, loai_xe, ten_xe, nam_san_xuat,
                loai_bang_lai, trang_thai, odometer_km, last_service_km, rental_price,
                created_at, updated_at)
               VALUES ($1, $2, $3, 'motorbike', $4, $5, $6, $7, 0, 0, $8,
                       NOW(), NOW())""",
            uid, branch_uuid, r["plate"], r["name"], year, loai, status, rental_price,
        )
        vehicles_out.append({
            "id": uid, "branch_id": branch_uuid,
            "rental_price": rental_price, "licence_wire": licence_wire,
        })
    print(f"  vehicles: {len(vehicles_out)}")
    return vehicles_out


async def seed_fee_plans(conn):
    rows = read_csv("fee_plans")
    out = {}
    for r in rows:
        uid = uuid.uuid4()
        amount = Decimal(r["amount"])
        await conn.execute(
            """INSERT INTO fee_plans (id, name, licence, amount, created_at, updated_at)
               VALUES ($1, $2, $3, $4, NOW(), NOW())""",
            uid, r["name"], r["licence"], amount,
        )
        out[r["licence"]] = (uid, amount)
    print(f"  fee_plans: {len(rows)}")
    return out


async def seed_promotions(conn):
    rows = read_csv("promotions")
    by_licence = {"A": [], "A1": []}
    for r in rows:
        uid = uuid.uuid4()
        raw = r.get("appliesTo_csv") or ""
        applies = [FEE_PLAN_TO_LICENCE.get(x.strip(), x.strip())
                   for x in raw.split("|") if x.strip()] or ["A", "A1"]
        applies = [a for a in applies if a in ("A", "A1")]
        discount = Decimal(r.get("discount") or 0)
        await conn.execute(
            """INSERT INTO promotions (id, ma_khuyen_mai, ten_khuyen_mai, loai_khuyen_mai,
                gia_tri, is_active, is_partner, applies_to_csv, created_at, updated_at)
               VALUES ($1, $2, $3, 'fixed', $4, TRUE, FALSE, $5, NOW(), NOW())""",
            uid, r["id"].upper().replace("-", ""), r["name"], discount, "|".join(applies),
        )
        for lic in applies:
            by_licence[lic].append((uid, discount, r["name"]))
    print(f"  promotions: {len(rows)}")
    return by_licence


